let opcao = 4;

switch (opcao) {
    case 0:
        console.log("Para funções de minha conta")
        break;
    case 1:
        console.log("Para funções de cartão")
        break;
    case 2:
        console.log("Para funções de faturas")
        break;
    case 3:
        console.log("Para funções de reclamações")
        break;
    default:
        console.log("Opção inválida") 
        break; 
}